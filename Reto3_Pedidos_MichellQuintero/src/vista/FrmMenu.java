    //Confirma las validaciones de clientes para poder comprar productos
    public void ValidarEntrada()
    {
         if(this.jPanelJuridica.isVisible()==true)
        {
            if (this.ComparandoPersonaJ()==true) {
               this.InsertarPersonaJ();
               LimpiarTexto();
               FrmProductos abrirForm= new FrmProductos();
               abrirForm.setVisible(true);
               this.dispose();
            }else if(this.ComparandoPersonaJ()==false)
            {
                int salida=JOptionPane.showConfirmDialog(null,"Desea Continuar?", "Usted ya esta registrado en DB",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
                
                if (salida==0) {
                    
                    JOptionPane.showMessageDialog(null, "Logueo Exitoso");
                    LimpiarTexto();
                    FrmProductos abrirForm= new FrmProductos();
                    abrirForm.setVisible(true);
                    this.dispose();
                }else if(salida==1)
                {
                    LimpiarTexto();
                }
            }   
        }else if(this.jPanelNatural.isVisible()==true){
            
            if (this.ComparandoPersonaN()==true) {
                this.InsertarPersonaN();
                LimpiarTexto();
                FrmProductos abrirForm= new FrmProductos();
                abrirForm.setVisible(true);
                this.dispose();
            }else if(this.ComparandoPersonaN()==false)
            {
                int salida=JOptionPane.showConfirmDialog(null,"Desea Continuar?", "Usted ya se encuentra registrada en la base de datos",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
                
                if (salida==0) {
                    
                    JOptionPane.showMessageDialog(null, "Logueo Exitoso");
                    LimpiarTexto();
                    FrmProductos abrirForm= new FrmProductos();
                    abrirForm.setVisible(true);
                    this.dispose();
                    
                }else if(salida==1)
                {
                    LimpiarTexto();
                }
            }
            
        }
        
    }
    //Valida que cada casilla del formulario no este vacia o nula
    public boolean ValidarCamposVacios()
    {
        boolean flag=false;
        
        if(this.jPanelJuridica.isVisible()==true)
        {
            //Validar que el texto telefono no este vacio
            if(this.txtTelefonoN.getText().isEmpty())
            {
                this.lblTel.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblTel.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            //Validar que correo no este vacio y que tenga los caracteres @ y .
            if(this.txtCorreoN.getText().isEmpty())
            {
                this.lblCorreo.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else if(!this.txtCorreoN.getText().contains("@")||!this.txtCorreoN.getText().contains("."))
            {
                this.lblCorreo.setText("*");
                this.lblCorreoV.setText("Correo no valido debe contener un @ y un .");
                flag=false;
            }else
            {
                this.lblCorreo.setText("");
                this.lblCampo.setText("");
                this.lblCorreoV.setText("");
                flag=true;
            }
            //Validar que la direccion no este vacia
            if(this.txtDireccionN.getText().isBlank())
            {
                this.lblDire.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblDire.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            //Validar que la razon Social no esta vacio
            if (this.txtRazonSocial.getText().isBlank()) {
                this.lblRazon.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblRazon.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            //Validar que el nit no este vacio
            if(this.txtNit.getText().isBlank())
            {
                this.lblCampo.setText("*Campos requeridos");
                this.lblNit.setText("*");
                flag=false;

            }else
            {
                this.lblNit.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            
            return flag;
        }else if(this.jPanelNatural.isVisible()==true)
        {
           //Validar que el texto telefono no este vacio
            if(this.txtTelefonoN.getText().isEmpty())
            {
                this.lblTel.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblTel.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            //Validar que correo no este vacio y que tenga los caracteres @ y .
            if(this.txtCorreoN.getText().isEmpty())
            {
                this.lblCorreo.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else if(!this.txtCorreoN.getText().contains("@")||!this.txtCorreoN.getText().contains("."))
            {
                this.lblCorreo.setText("*");
                this.lblCorreoV.setText("Correo no valido debe contener un @ y un .");
                flag=false;
            }else
            {
                this.lblCorreo.setText("");
                this.lblCampo.setText("");
                this.lblCorreoV.setText("");
                flag=true;
            }
            //Validar que la direccion no este vacia
            if(this.txtDireccionN.getText().isBlank())
            {
                this.lblDire.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblDire.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            //validar que el nombre1 no este vacio
            if(this.txtNombre1.getText().isBlank())
            {
                this.lblNom1.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblNom1.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            //Validar que el apellido 1 no este vacio
            if(this.txtApellido1.getText().isEmpty())
            {
                this.lblApell1.setText("*");
                this.lblCampo.setText("*Campos requeridos");
                flag=false;
            }else
            {
                this.lblApell1.setText("");
                this.lblCampo.setText("");
                flag=true;
            }
            // validar que el documento noi este vacio
            if (this.txtDocumento.getText().isBlank()) {

               this.lblDocum.setText("*");
               this.lblCampo.setText("*Campo requerido");
               flag=false;
           }else
           {
               this.lblDocum.setText("");
               this.lblCampo.setText("");
               flag=true;
           }
            
            return flag;
        }
  
        return flag;
    }
    
    
    
