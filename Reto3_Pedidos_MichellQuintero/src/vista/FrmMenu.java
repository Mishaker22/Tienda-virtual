    public boolean ComparandoPersonaN()
    {
        boolean flag=false;
        Natural n1= new Natural();
        Administrar admi=new Administrar();
        n1.setDocumentoId(Integer.parseInt(this.txtDocumento.getText()));
        
        List<Natural> lista= admi.ListarNaturales();
        for (int i = 0;  i<lista.size(); i++) 
        {
            if (lista.get(i).getDocumentoId()==n1.getDocumentoId())
            {
                flag=false;
                ListaId.add(String.valueOf(lista.get(i).getIdCliente()));
                return flag;
            }else{
                flag = true;
                
            }
        }
 
        return flag;
    }
