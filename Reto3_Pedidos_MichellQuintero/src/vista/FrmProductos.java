    public void QuitarProductoCarrito()
    {
        int fila=this.jTableCarrito.getSelectedRow();
        int cant=Integer.parseInt(this.jTableCarrito.getValueAt(fila, 2).toString());
        int id=Integer.parseInt(this.jTableCarrito.getValueAt(fila, 0).toString());
        int cantP, suma;
        float precio=Float.parseFloat(this.jTableCarrito.getValueAt(fila, 3).toString());
        float aux, restaT;
        for (int i = 0; i <this.jTablaProductos.getRowCount(); i++) {
            
            if (id==Integer.parseInt(this.jTablaProductos.getValueAt(i, 0).toString()))
            {
                cantP=Integer.parseInt(this.jTablaProductos.getValueAt(i, 3).toString());
                suma=cantP+cant;
                aux=Float.parseFloat(this.txtTotal.getText());
                restaT=aux-precio;
                this.jTablaProductos.setValueAt(suma, i, 3);
                this.txtTotal.setText(String.valueOf(restaT));
                DefaultTableModel modelo=(DefaultTableModel)this.jTableCarrito.getModel();
                modelo.removeRow(fila);
            }
        }
    
    }
