    public void InsertarPedido(){
        Pedido p=new Pedido();
        Administrar admi= new Administrar();
        
        p.setIdCliente(Integer.parseInt(this.lbIdCliente.getText()));
        p.setFechaPedido(LocalDate.now());
        p.setTotal(Float.parseFloat(this.txtTotal.getText()));
        p.setEstado(Pedido.Estado.PENDIENTE);
        
        int r= admi.AgregarPedido(p);
        if (r==1) {
            JOptionPane.showMessageDialog(this,"Se agrego el pedido correctamente");
        }else
        {
            JOptionPane.showInputDialog(this,"Error al cargar el pedido");
        }
    }
