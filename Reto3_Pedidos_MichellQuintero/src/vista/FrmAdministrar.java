    public void InsertarProducto()
    {
        LineaProducto lp= new LineaProducto();
        Administrar admi=new Administrar();
        
        lp.setDenominacion(this.txtDenominacion.getText());
        lp.setProveedor(this.txtProveedor.getText());
        lp.setCantidad(Integer.parseInt(this.txtCantidad.getText()));
        lp.setPrecio(Float.parseFloat(this.txtPrecio.getText()));
        
        int r= admi.AgregarProducto(lp);
        if (r==1) {
            JOptionPane.showMessageDialog(this,"Se agrego un producto");
        }else
        {
            JOptionPane.showInputDialog(this,"Error al cargar el producto");
        }
    }
    public void CargarDatosTabla()
    {
        LineaProducto lp= new LineaProducto();
        lp.Listar(this.tablaLista);
        lp.LimpiarTabla(this.tablaLista);
        lp.Listar(this.tablaLista);
        
    }
