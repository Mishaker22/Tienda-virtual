    public void EliminarFila()
    {
        int fila=this.tablaLista.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione una fila");
        }else
        {
            String id= String.valueOf(this.tablaLista.getValueAt(fila, 0));
            int idAux=Integer.parseInt(id);
            Administrar admi=new Administrar();
            admi.Eliminar(idAux);
            JOptionPane.showMessageDialog(this, "Se Elimino la fila Correctamente!!");
            CargarDatosTabla();
        }
        LimpiarTexto();
    }
    

