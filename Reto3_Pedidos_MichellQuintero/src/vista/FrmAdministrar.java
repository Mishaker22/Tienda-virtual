    public boolean ComparandoProductos()
    {
        boolean flag=false;
        LineaProducto p1= new LineaProducto();
        Administrar admi=new Administrar();

        p1.setDenominacion(this.txtDenominacion.getText());
        p1.setProveedor(this.txtProveedor.getText());
        p1.setCantidad(Integer.parseInt(this.txtCantidad.getText()));
        p1.setPrecio(Float.parseFloat(this.txtPrecio.getText()));
        
        List<LineaProducto> lista= admi.Listar();
        for (int i = 0;  i<lista.size(); i++) 
        {
            if (lista.get(i).getDenominacion().equals(p1.getDenominacion())&&
                    lista.get(i).getProveedor().equals(p1.getProveedor())&&
                    lista.get(i).getCantidad()==p1.getCantidad()&&
                    lista.get(i).getPrecio()==p1.getPrecio())
            {
                JOptionPane.showMessageDialog(this, "Este producto ya existe! ");
                flag=false;
                break;
            }else{
                flag = true;
            }
        }
 
        return flag;
    }
