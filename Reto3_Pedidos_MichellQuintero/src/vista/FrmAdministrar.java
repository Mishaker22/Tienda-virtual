    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        int fila=this.tablaLista.getSelectedRow();
        if (fila==-1) 
        {
            JOptionPane.showMessageDialog(this, "Selecciona una fila");
        }else
        {
            this.lbIdProducto.setText(String.valueOf(this.tablaLista.getValueAt(fila, 0)));
            this.txtDenominacion.setText((String)this.tablaLista.getValueAt(fila, 1));
            this.txtProveedor.setText((String)this.tablaLista.getValueAt(fila, 2));
            this.txtCantidad.setText(String.valueOf(this.tablaLista.getValueAt(fila, 3)));
            this.txtPrecio.setText(String.valueOf(this.tablaLista.getValueAt(fila, 4)));
            if (this.ValidarTextosVacios()==true) {
                this.btnGuardar.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

